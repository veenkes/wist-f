interface ThemeProviderProps {
  children: ReactNode;
}

export const ThemeProvider: React.FC<ThemeProviderProps> = ({ children }) => {
  // Initialize theme and language from localStorage
  const [theme, setTheme] = useState<Theme>(() => {
    const savedTheme = localStorage.getItem('wist-theme');
    return (savedTheme === 'dark' || savedTheme === 'light') ? savedTheme : 'light';
  });

  const [language, setLanguage] = useState<Language>(() => {
    const savedLanguage = localStorage.getItem('wist-language');
    return (savedLanguage === 'en' || savedLanguage === 'ru' || savedLanguage === 'uz') ? savedLanguage : 'en';
  });

  // Apply theme to document and save to localStorage
  useEffect(() => {
    const root = window.document.documentElement;
    root.classList.remove('light', 'dark');
    root.classList.add(theme);
    localStorage.setItem('wist-theme', theme);
  }, [theme]);

  // Save language to localStorage
  useEffect(() => {
    localStorage.setItem('wist-language', language);
  }, [language]);

  const toggleTheme = () => {
    setTheme(prev => prev === 'light' ? 'dark' : 'light');
  };

  const toggleLanguage = () => {
    setLanguage(prev => {
      if (prev === 'en') return 'ru';
      if (prev === 'ru') return 'uz';
      return 'en';
    });
  };

  const t = (key: string, params?: Record<string, string | number>): string => {
    // Fallback to English if translation not found
    let translation = translations[language]?.[key as keyof typeof translations['en']]
      || translations['en'][key as keyof typeof translations['en']]
      || key;

    // Replace placeholders if params provided
    if (params) {
      Object.entries(params).forEach(([paramKey, value]) => {
        translation = translation.replace(`{${paramKey}}`, String(value));
      });
    }

    return translation;
  };

  const value = {
    theme,
    language,
    toggleTheme,
    toggleLanguage,
    setLanguage,
    t,
  };

  return <ThemeContext.Provider value={value}>{children}</ThemeContext.Provider>;
};